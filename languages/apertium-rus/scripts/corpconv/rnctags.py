def rnc2cg(parse):
	# convert each RNC tag to a CG tag
	# exclude unconvertible tags
	global weeDict

	outParse = {}
	for key in parse.keys():
		outParse[key] = []
		for tag in parse[key]:
			if tag in weeDict:
				if "." in weeDict[tag]:
					splitTags = weeDict[tag].split(".")
					for splitTag in splitTags:
						outParse[key].append(splitTag)
				else:
					outParse[key].append(weeDict[tag])
	
	#print(outParse)
	
	return outParse

